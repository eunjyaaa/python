# 기본 자료형

# 1. 숫자형: 정수형 / 실수형
print(1)
print(-2)
print(type(0))

print(-1.5)
print(3.14)
print(type(3.14))

# 2. 문자형: string
print(type("문자열입니다."))
print('문자열인디요')
print(type("1.0"))
print("")
print("\"따옴표 쓰고 싶어잉\"") # escape 문자열 == \
print("'따옴표를 쓰겠습니다.'")

# 3. 불린형
print(True) # 참
print(False) # 거짓
print(type(True))